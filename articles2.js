一、剑铭
淬火几番终作犁
十年磨就刃自饴
人间若问真筋骨
不在锋芒在钝时
二、茶则
沉浮三沸见澄明
浓淡皆凭火候成
世上荣枯壶里看
几片青霜是旧盟
三、砚铭
