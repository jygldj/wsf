《蜀道畅》VS《蜀道难》
诗｜乙巳年五月十五日

噫吁嚱！古云蜀道难，难于上青天，
而今蜀道畅，畅若行平川！
昔日鸟道萦绝壁，猿猱欲度愁攀援；
今朝天堑变通途，高铁如龙穿山峦。
忆往昔，剑门峥嵘而崔嵬，一夫当关万夫哀。
栈道钩连危石间，商旅踽踽心胆寒。
滟滪堆前浪拍天，舟楫难渡泪潸然。
蚕丛鱼凫开国远，崎岖古道岁月残。
看如今，银桥飞架云霭间，长虹卧波连蜀川。
雅西高速盘玉带，云端天路入画卷。
隧道逶迤穿层嶂，瞬间跨越万重山。
成都天府机场立，银鹰展翅翔宇寰。
中欧班列鸣笛去，贯通丝路连亚欧。
蜀江水碧蜀山青，高铁穿梭似流星。
朝发锦城霞光里，暮至长安灯火明。
昔时蜀道，阻绝了多少游子归乡梦；
今日通途，承载着万千希望向未来。
科技筑就通天路，蜀地繁荣展新颜。
蜀道之畅，畅在交通之便捷，
畅在经济之腾飞，
畅在民心之欢悦。
且放豪情云端上，笑看蜀道谱新章！
蜀道畅，畅四方，锦绣前程万里长！


