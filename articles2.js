沁园春·黄河铁桥怀古
词｜甲辰年八月十五日

铁骨横波，寒星铸铆，浊浪吞秋。
任左公柳老，驼铃锈蚀；洋灰缝裂，岁月奔流。
曾渡征鞍，也栖胡雁，暗把河声锁铁喉。
斜阳里，看钢梁斑驳，犹刻庚戌。
当年德匠西来，算此物、竟成第一楼。
纵轮机震耳，难惊白塔；烽烟蔽日，未改金瓯。
