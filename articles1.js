买来含住，却尝出
五块钱一公斤的旧时光，
早已通货膨胀。
四、河神的宴席
石羊河干涸那年，
我在龟裂的河床上捡到：
半片驴肉馆的蓝花碗（缺角处粘着陈年酱汁）
