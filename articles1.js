面观》散文
散文｜乙巳年五月十五日

我总疑心，中国人的魂灵是面做的。
十年前在苏州初尝清汤面，瓷碗里沉着几茎银丝，汤色澄澈如虎丘的晨雾。那时不解“至味在淡”的道理，只道是江南人吝啬油水。后来才懂得，这一碗清汤里煮着《红楼梦》里妙玉的梅花雪水，是需用十年光阴才能泡开的禅机。
无锡王兴记的阳春面又是另一番光景。面汤比太湖更空明，葱花浮在碗中，竟像惠山寺塔顶的积雪。浇头是素的，笋片、豆干、木耳，排布如倪云林的枯笔山水。吃着吃着，忽然想起评弹艺人指间的三弦——那面汤里颤动的油星子，可不就是弦上滑落的泛音？
重庆小面却似川剧变脸。才见红油如赤霞铺满碗底，转眼花椒已在舌尖炸开满天星斗。麻辣之气直冲囟门，恍惚看见嘉陵江上的纤夫，正踩着锣鼓点把太阳拉过十二峰。
最是兰州牛大碗教人牵魂。清晨六点，马子禄的拉面师傅已把面团摔得啪啪作响，活像秦腔里的花脸一声吼破天。辣子需泼得艳若敦煌飞天裙裾，醋要淋得爽似黄河九曲。一清二白的汤色里，浮沉着祁连山的月光。西北人吃面要蹲在凳子上，这般架势，倒像是捧着《史记》在读——面碗里分明漾着霍去病痛饮的瀚海。
三碗面下肚，忽然了悟：清汤面是《金刚经》，阳春面是《心经》，麻辣小面是《坛经》，牛大碗却是整部《大藏经》。筷子提起时牵丝连缕，哪一根不是三千大千世界？
面禅三昧：苏州面碗倒映着园林漏窗，重庆红油沉淀着码头往事，兰州面汤里游动着丝路驼铃。一碗面端起来，便是捧起半部华夏文明史。
味觉参悟：清汤之淡，是“看山还是山”的第三重境界。麻辣之痛，恰似明心见性时的当头棒喝。酸辣交攻处，藏着“烦恼即菩提”的密意。
匠道相济：拉面师傅摔打面团时的节奏，暗合《黄帝内经》的呼吸法；苏州面汤的文火，炖着陆羽《茶经》的火候诀。


